class ArithmaticOperator{
    public static void main(String[] args){
        int a=10;
        int b=20;
        int add=a+b;
        int sub=a-b;
        int mul=a*b;
        int div=a/b;
        int mod=a%b;
        System.out.println("Add:"+add);
        System.out.println("Sub:"+sub);
        System.out.println("Mul:"+mul);
        System.out.println("Div:"+div);
        System.out.println("Mod:"+mod);
        
    }
}